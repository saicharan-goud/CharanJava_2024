package Practice;

public class switchusage {
    public static void main(String[] args) {
        int day = 22;
        switch (day) {
            case 1:
            System.out.println("Monday");
            break;
            case 2:
            System.out.println("Tuesday");
            break;
            case 3:
            System.out.println("wednesday");
            break;
            default:
            System.out.println("Holiday");

        }
    }
    
}
