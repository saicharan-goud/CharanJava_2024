package Practice.Encapsulation;

class Person {
    private String name;
    private String country;
    private int age;

    public String getName(){
        return name;
    }
    public void setName(String name){
        this.name=name;
    }
    public String getCountry(){
        return country;
    }
    public void setCountry(String country){
        this.country=country;
    }
    public int setAge(){
        return age;
    }
    public void getAge(){
        this.age = age;
    }
    
}
